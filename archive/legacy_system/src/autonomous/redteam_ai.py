    def _mutate_string_encoding(self, code: str) -> Tuple[str, bool]:
        """Encode strings to avoid signatures"""

        # Base64 encode suspicious strings
        suspicious_patterns = ["system", "exec", "eval", "shell"]

        mutated = code
